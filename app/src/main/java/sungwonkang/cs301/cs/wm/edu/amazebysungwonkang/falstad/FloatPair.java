/**
 * Trivial class to hold to double values. Used in FirstPersonDrawer in a static method.
 *
 */
public class FloatPair {
    public double p1, p2;
    
    /**
     * Constructor
     * @param pp1
     * @param pp2
     */
    FloatPair(double pp1, double pp2) {
	p1 = pp1;
	p2 = pp2;
    }
}
